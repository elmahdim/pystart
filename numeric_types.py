x = 6
y = 1.6

print("=======")
print("x =", x)
print("y =", y)
print("=======")

print("⇒ sum of x + y:", x + y)
print("⇒ difference of x - y:", x - y)
print("⇒ product of x * y:", x * y)
print("⇒ quotient of x / y:", x/y)
print("⇒ (floored) quotient of x // y:", x // y)
print("⇒ -x, x negated:", -x)
print("⇒ +x, x unchanged:", +x)
print("⇒ abs(x), absolute value or magnitude of x:", abs(x))
print("⇒ int(y), y converted to integer:", int(y))
print("⇒ float(x), x converted to floating point:", float(x))
print("⇒ pow(x, 2), x to the power y:", pow(x, 2))
print("⇒ x ** 2, x to the power y:", x ** 2)
